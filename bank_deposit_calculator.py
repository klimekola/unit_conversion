k0 = float(input('Enter initial capital: '))
m = float(input('Enter the number of capitalization: : '))
n = float(input('Enter the number of years: '))
r= float(input('Enter the annual percentage rate in decimal form: '))

def K(k0,m,n,r): 
    return k0*(1+r/m)**(m*n)

print('Final capital', round(K(k0,m,n,r),2))
